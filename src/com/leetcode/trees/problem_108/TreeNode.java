package com.leetcode.trees.problem_108;

public class TreeNode {

    public int val;

    TreeNode left, right;

    public TreeNode(int val, TreeNode left, TreeNode right) {
        this.val = val;
        this.left = left;
        this.right = right;
    }
    public TreeNode(int val) {
        this.val = val;
    }
}